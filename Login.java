    private void CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String username = jtuser.getText();
        String password = new String(jpassword.getPassword());
        // Check if username or password is empty
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username or password cannot be empty!");
            return;
        }       
        // Check if username already exists
        if (credentials.containsKey(username)) {
            JOptionPane.showMessageDialog(null, "Account is already existed, try another!");
        } else {
            // Add username-password pair to credentials
            credentials.put(username, password);
            // Set initial balance for the new account
            balances.put(username, 5000.0);
            JOptionPane.showMessageDialog(null, "Account created successfully!");
        }
    }//GEN-LAST:event_CreateActionPerformed

