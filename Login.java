    private void bloginActionPerformed(java.awt.event.ActionEvent evt) {                                       
        String username = jtuser.getText();
        String password = new String(jpassword.getPassword());
        
        // Check if username or password is empty
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, " Warning! Username or password cannot be empty. (CREATE ONE FIRST, if you dont have an account.)\n *JUST TYPE ANYTHING IN THE USERNAME & PASSWORD THEN PRESS CREATE ACCOUNT*\n *ACCOUNT WILL BE SAVED*");
            return;
        }
        
        // Check if username exists
        if (credentials.containsKey(username)) {
            // Check if password is correct
            if (credentials.get(username).equals(password)) {
                JOptionPane.showMessageDialog(null, "Login successful!");
                // Proceed to main application window or perform other actions
               Menu menuFrame = new Menu(username);
               menuFrame.setBalances(balances);
               menuFrame.setVisible(true);
               dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Incorrect password!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Account does not exist.(REGISTER FIRST!.)\n 1.JUST TYPE ANYTHING IN THE EMAIL & PASSWORD THEN PRESS CREATE ACCOUNT \n 2.ACCOUNT WILL BE REGISTERED \n 3.CLICK LOG IN AGAIN");
        }
                                            
    }                                      

